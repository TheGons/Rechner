
public class Rechenoperationen {
	
	public float Addition(float x, float y)
	{
		return(x+y);
	}
	
	public float Multiplikation(float x, float y)
	{
		return(x*y);
	}
	
	public float Division(float x, float y)
	{
		return(x/y);
	}
	
	public float Subtraktion(float x, float y)
	{
		return(x-y);
	}
}
